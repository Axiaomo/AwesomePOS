<script>
import axios from "axios";
export default {
  data() {
    return {
      tableDate: [],
      type0Goods0: [],
      type0Goods1: [],
      type0Goods2: [],
      type0Goods3: [],
      oftenGoods: [],
      totalMoney: 0,
      totalCount: 0
    };
  },
  mounted() {
    //   高度设置
    var orderHeight = document.body.clientHeight;
    document.getElementById("order-list").style.height = orderHeight + "px";
  },
  components: {},

  computed: {},
  // 服务器拉取数据
  created() {
    //   常用商品
    axios
      .get("http://jspang.com/DemoApi/oftenGoods.php")
      .then(res => {
        return (this.oftenGoods = res.data);
      })
      .catch(error => {
        alert("不能访问");
      });
    // 分类商品
    axios
      .get("http://jspang.com/DemoApi/typeGoods.php")
      .then(res => {
        this.type0Goods0 = res.data[0];
        this.type0Goods1 = res.data[1];
        this.type0Goods2 = res.data[2];
        this.type0Goods3 = res.data[3];
      })
      .catch(error => {
        alert("不能访问1");
      });
    // 点击右侧添加到左侧列表
  },
  methods: {
    // 模拟结账
    checkout() {
      if (this.totalMoney != 0) {
        this.tableDate = [];
        this.totalCount = 0;
        this.totalMoney = 0;
        this.$message({
          message: "结账成功，感谢你又为店里除了一份力!",
          type: "success"
        });
      } else {
        this.$message.error("请先选择商品!");
      }
    },
    // 删除所有商品
    dele() {
      this.tableDate = [];
      (this.totalMoney = 0), (this.totalCount = 0);
    },
    // 删除单个商品
    delSingleGoods(goods) {
      let arr = goods.count;
      if (arr == 1) {
        this.tableDate = this.tableDate.filter(a => a.goodsId != goods.goodsId);
      } else {
        var exTableData = this.tableDate;
        for (var i = 0; i < exTableData.length; i++) {
          if (exTableData[i].goodsId == goods.goodsId) {
            exTableData[i].count--;
          }
        }
        this.tableDate = exTableData;
      }
      this.AllMoney();
    },
    // 添加商品
    addOrderList(goods) {
      this.totalCount = 0;
      this.totalMoney = 0;
      // 商品是否已经存在于订单列表中
      let isHave = false;
      for (let i = 0; i < this.tableDate.length; i++) {
        if (this.tableDate[i].goodsId == goods.goodsId) {
          isHave = true;
        }
      }
      // 根据判断我们在进行业务逻辑
      if (isHave) {
        // 存在进行数量添加
        let arr = this.tableDate.filter(a => a.goodsId == goods.goodsId);
        arr[0].count++;
      } else {
        // 不存在就推入数组
        let newGoods = {
          goodsId: goods.goodsId,
          goodsName: goods.goodsName,
          price: goods.price,
          count: 1
        };
        this.tableDate.push(newGoods);
      }
      this.AllMoney();
    },
    // 计算价格
    AllMoney() {
      this.totalCount = 0;
      this.totalMoney = 0;
      if (this.tableDate) {
        this.tableDate.forEach(element => {
          this.totalCount += element.count;
          this.totalMoney = this.totalMoney + element.price * element.count;
        });
      } else {
        0;
      }
    }
  }
};
</script>
