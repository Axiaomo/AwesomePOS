          <el-tabs>
            <el-tab-pane label="汉堡">
              <div>
                <ul class='cookList'>
                  <li v-for="coockList in type0Goods0" @click="addOrderList(coockList)">
                    <span class="foodImg"><img :src="coockList.goodsImg" width="100%"></span>
                    <span class="foodName">{{coockList.goodsName}}</span>
                    <span class="foodPrice">{{coockList.price}}</span>
                  </li>
